    public static void solveMaze(int[][] maze, int[] position, int[] goal) {
        int steps = 0;
        while (!isAtGoal(position, goal) && steps <= 200) {
            int action = chooseAction(maze, position);
            performAction(position, action);
            steps++;
        }

        if (isAtGoal(position, goal)) {
            System.out.println("Цель выполнена за " + steps + " шагов.");
        } else {
            System.out.println("Шаги превышают 200.");
        }
    }

    public static int chooseAction(int[][] maze, int[] position) {
        Random random = new Random();
        int action = random.nextInt(4); // Генерация случайного действия: 0 - вперед, 1 - налево, 2 - направо, 3 - ничего не делать
        if (action == 0 && isForwardValid(maze, position)) {
            return 0; // Вперед
        } else if (action == 1) {
            return 1; // Налево
        } else if (action == 2) {
            return 2; // Направо
        } else {
            return 3; // Ничего не делать
        }
    }

    public static boolean isForwardValid(int[][] maze, int[] position) {
        int x = position[0];
        int y = position[1];
        return x + 1 < 32 && maze[x + 1][y] != 1; // Проверка наличия препятствия впереди
    }

    public static void performAction(int[] position, int action) {
        // Выполнение действия
        int x = position[0];
        int y = position[1];
        if (action == 0) {
            position[0] = x + 1; // Движение вперед
        } else if (action == 1) {
            position[1] = y - 1 < 0 ? 31 : y - 1; // Поворот налево
        } else if (action == 2) {
            position[1] = y + 1 > 31 ? 0 : y + 1; // Поворот направо
        }
    }

    public static boolean isAtGoal(int[] position, int[] goal) {
        return position[0] == goal[0] && position[1] == goal[1];
    }
